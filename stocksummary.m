win1guihandle = win1;
win1guidata = guidata(win1guihandle);

index = get(win1guidata.listbox1,'Value');
contents = get(win1guidata.listbox1,'String');
company_name = cell2mat(contents(index));

set(handles.texttop,'String',company_name);

mylist = {'MMM','AA','AXP','T','BAC','BA','CAT','CVX','CSCO','KO','DD','XOM',...
    'GE','HPQ','HD','INTC','IBM','JNJ','JPM','KFT','MCD','MRK','MSFT','PFE',...
    'PG','TRV','UTX','VZ','WMT','DIS'};
stock_symbol = cell2mat(mylist(index));

last_trade = get_last_trade(stock_symbol);

set(handles.text2,'String',last_trade);

url_string = strcat('http://www.bloomberg.com/apps/quote?ticker=',stock_symbol);

out_table = getTableFromWeb_mod(url_string, 18);


set(handles.text12,'String',out_table{2,2});
set(handles.text13,'String',out_table{3,2});
set(handles.text14,'String',out_table{4,2});
set(handles.text15,'String',out_table{5,2});
set(handles.text16,'String',out_table{6,2});
set(handles.text17,'String',out_table{7,2});
set(handles.text18,'String',out_table{8,2});
set(handles.text19,'String',out_table{9,2});


% Update handles structure
guidata(hObject, handles);

